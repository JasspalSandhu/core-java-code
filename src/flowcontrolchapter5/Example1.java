package flowcontrolchapter5;

class Example1 {

	public static void main(String args[]) {
		int x = 6;
		if (x == 1) {
			System.out.println("x equals 1");
		} else if (x == 2) {
			System.out.println("x equals 2");
		} else if (x == 3) {
			System.out.println("x equals 3");
		} else {
			System.out.println("No idea what x is");
		}

	}
}
